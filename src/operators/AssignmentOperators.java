package operators;

public class AssignmentOperators {
    public static void main(String[] args) {

        int a = 7, b = 3;

        // b = b + a

        // Addition
        b += a;
        System.out.println(b);

        // Subtraction
        b -= a;
        System.out.println(b);

        // Multiplication
        b *= a;
        System.out.println(b);

        // Division
        b /= a;
        System.out.println(b);

        // Remainder
        b %= a;
        System.out.println(b);
    }
}
